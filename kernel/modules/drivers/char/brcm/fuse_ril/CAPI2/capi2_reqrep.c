Result_t CAPI2_SerializeReqRsp(CAPI2_ReqRep_t* req_rsp, UInt32 tid, MsgType_t msgId, UInt8 clientID,  Result_t result, char** stream, UInt32* len)
{
	XDR xdrs;
	UInt32 maxPktSize =0;
	struct xdr_discrim* entry = xdr_fast_lookup((enum_t)msgId);


#ifndef  BYPASS_IPC 
	PACKET_BufHandle_t bufHandle;
	if(entry == NULL)
	{
		_DBG_(CAPI2_TRACE("CAPI2_SerializeReqRsp: failed!"));
		return RESULT_ERROR;
	}
	else
	{
		maxPktSize = CAPI2_GetMaxPktSize(INTERFACE_CAPI2, entry->maxMsgSize);
	}

	bufHandle = CAPI2_PACKET_AllocateBuffer (INTERFACE_CAPI2, (maxPktSize==0) ? MAX_MSG_STREAM_SIZE : maxPktSize, 0);
	if(!bufHandle)
	{
		_DBG_(CAPI2_TRACE("CAPI2_SerializeReqRsp buffer allocation fail!!! = 0x%x, 0x%x\r\n", msgId, tid));
		//If you see this assert then likely remote processor does not free the buffers.
		//- Likely cause is CAPI2 Task on remote processor is stuck waiting on a Queue.
		//- or Watchdog issue where CAPI2 Task does not get chance to run.
		//- or Remote processor is completely DEAD
		#ifdef FUSE_COMMS_PROCESSOR
			xassert(REMOTE_AP_NOT_RESPONDING, msgId);
		#else
			xassert(REMOTE_CP_NOT_RESPONDING, msgId);
		#endif
		return RESULT_ERROR;
	}

	*stream = CAPI2_PACKET_GetBufferData(bufHandle);
	if(*stream == 0)
		return RESULT_ERROR;
#endif	

	/* Initialize the common part of the request/response */
	req_rsp->tid = tid;
	req_rsp->clientID = clientID;
	req_rsp->msgId = msgId;
	req_rsp->result = result;

#ifdef  BYPASS_IPC 
    maxPktSize = (entry && entry->maxMsgSize !=0 ) ? entry->maxMsgSize : MAX_MSG_STREAM_SIZE;
	/* Serialize request/response */
	*stream = (char*)ALLOC_STREAM(maxPktSize);
#endif

	if(DETAIL_LOG_ENABLED)
		xdrmem_create(&xdrs, *stream, (maxPktSize==0) ? MAX_MSG_STREAM_SIZE : maxPktSize, log_buf, MAX_LOG_BUFFER_SIZE, XDR_ENCODE);
	else
		xdrmem_create(&xdrs, *stream, (maxPktSize==0) ? MAX_MSG_STREAM_SIZE : maxPktSize, NULL, 0, XDR_ENCODE);

	result = (xdr_CAPI2_ReqRep_t(&xdrs, req_rsp)) ? RESULT_OK : RESULT_ERROR;

	if(result != RESULT_OK)
		_DBG_(CAPI2_TRACE("CAPI2_SerializeReqRsp: failed tid=%d cid=%d msgId=%d", tid, clientID, msgId));

	*len = xdr_getpos(&xdrs);
	xdr_destroy(&xdrs);

#ifndef WIN32
	CAPI2_LogBinaryData(req_rsp->msgId, (UInt8*)*stream, *len);
#endif

#ifndef  BYPASS_IPC 
	CAPI2_PACKET_SetBufferLength(bufHandle, *len);
	CAPI2_PACKET_SendData(0, INTERFACE_CAPI2, 0, bufHandle);
#endif
	return(result);
}

Result_t CAPI2_SerializeRsp(CAPI2_ReqRep_t* req_rsp, UInt32 tid, MsgType_t msgId, UInt8 clientID,  Result_t result, char** stream, UInt32* len)
{
	req_rsp->msgType = CAPI2_TYPE_RESPONSE;
	return CAPI2_SerializeReqRsp(req_rsp, tid, msgId, clientID, result, stream, len);
}


/* Deserialize the response stream passed by the IPC */
Result_t CAPI2_DeserializeReqRsp( char* stream, UInt32 stream_len, CAPI2_ReqRep_t** inReqRep, ResultDataBuffer_t**  inDataBuf)
{
	XDR *xdrs = NULL;
	CAPI2_ReqRep_t* reqRep = NULL;
	ResultDataBuffer_t* dataBuf;
	Result_t result = RESULT_OK;

	*inReqRep = NULL;
	*inDataBuf = NULL;

	dataBuf = (ResultDataBuffer_t*)capi2_malloc(sizeof(ResultDataBuffer_t));
	if(dataBuf)
	{
		xdrs = &dataBuf->xdrs;
		reqRep = &dataBuf->rsp;
		/* Deserialize the request/response */
		memset(reqRep, 0, sizeof(CAPI2_ReqRep_t));
		if(DETAIL_LOG_ENABLED)
			xdrmem_create( xdrs, stream, stream_len, log_buf, MAX_LOG_BUFFER_SIZE, XDR_DECODE );
		else
			xdrmem_create( xdrs, stream, stream_len, NULL, 0, XDR_DECODE );

		result = (xdr_CAPI2_ReqRep_t( xdrs, reqRep )) ? RESULT_OK : RESULT_ERROR; 
		
		if(result != RESULT_OK)
			_DBG_(CAPI2_TRACE("CAPI2_DeserializeReqRsp: failed dataBuf=%x stream=%x len=%d", (UInt32)dataBuf, (UInt32)stream, stream_len));

		*inReqRep = reqRep;
		*inDataBuf = dataBuf;
#ifndef WIN32
		CAPI2_LogBinaryData(reqRep->msgId, (UInt8*)stream, stream_len);
#endif
	}
	else
	{
		xassert(0,stream);
		result = RESULT_ERROR;
	}

	/* The AP side want to free up the deserialized buffer. */
	return(result);
}

