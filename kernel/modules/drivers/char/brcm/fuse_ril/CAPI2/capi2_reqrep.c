bool_t
xdr_CAPI2_ReqRep_t(XDR *xdrs, CAPI2_ReqRep_t* reqRep)
{
	struct xdr_discrim *entry = NULL;
	enum_t dscm = reqRep->msgId;
	/* Build the lookup table */
		if( xdr_build_lookup()&&
			xdr_u_long(xdrs, &reqRep->tid) &&
			xdr_u_char(xdrs, &reqRep->clientID) &&
			XDR_ENUM(xdrs, &reqRep->msgId, MsgType_t) &&
			XDR_ENUM(xdrs, &reqRep->respId, MsgType_t) &&
			XDR_ENUM(xdrs, &reqRep->result, Result_t) &&
			XDR_ENUM(xdrs, &reqRep->msgType, CAPI2_MsgType_t) &&
			XDR_ENUM(xdrs, &reqRep->msgDirection, CAPI2_Direction_t)
			)
	{
		XDR_LOG_RESET(xdrs, "[CAPI2] { msgId=");

		if(xdr_union(xdrs, (enum_t *)&dscm, (caddr_t)(void *)&(reqRep->req_rep_u), 
							CAPI2_ReqRep_dscrm, NULL_xdrproc_t, &entry, xdr_fast_lookup))
		{
			if(entry != NULL)
			{
				reqRep->unsize = entry->unsize;

				if(xdrs->x_op == XDR_ENCODE || xdrs->x_op == XDR_DECODE)
				{
					if(BASIC_LOG_ENABLED)
					{
						char tempStr[20];
						if(reqRep->msgId == MSG_CAPI2_ACK_RSP)
							snprintf(tempStr,20,"pend=%d",reqRep->req_rep_u.CAPI2_Ack_Rsp.isCapiPending);
						else
							strncpy(tempStr,"",20);

							_DBG_(CAPI2_TRACE("%cCAPI2 %s%c msgid=%d str=%s tid %d cid %d result %d %s\r\n", 
								(gCapi2Type == CAPI2_APPS) ? '[' : '<',
								(xdrs->x_op == XDR_DECODE)?"DE":"EN",
								(gCapi2Type == CAPI2_APPS) ? ']' : '>',
								reqRep->msgId, 
								(entry->debugStr)?entry->debugStr:"<>", 
								reqRep->tid, reqRep->clientID, reqRep->result,
								tempStr));
					}
				}

#ifdef XDR_DETAIL_LOG_ENABLED
				if(DETAIL_LOG_ENABLED)
				{
					if(reqRep->msgId != MSG_CAPI2_ACK_RSP && (xdrs->x_op == XDR_ENCODE || xdrs->x_op == XDR_DECODE))
					{

						if((strlen(log_buf)+3) < MAX_LOG_BUFFER_SIZE)
							strncat(log_buf,"}\r\n",3);

						_DBG_(Capi2_DebugOutputString(log_buf));
					}
				}
#endif

			}
			return(TRUE);
		}
	}
	
	xassert(0, dscm);
	return(FALSE);
}

