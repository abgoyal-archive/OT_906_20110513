static struct  xdr_discrim*** tableBase = NULL;
/*
 *	Lookup table indexed by MsgType and organized into two levels
 *	to optimize the memory usage.
	Lookup table entry for MsgType = MSG_PBK_GETALPHA_REQ = 0xf601
	tableBase(+0x00)-->NULL
		|
		|(+0x01)
		------->NULL
		|
		|		...
		|
		|(+0xf6)		   (+0x00)
		|------>tableBase2------->NULL
		|			|(+0x01)
		|			|------------>&CAPI2_ReqRep_dscrm[1]
		|			|
		|			|------------>...
		|			|(+0xff)
		|			|------------>NULL
		|
		|(0xff)
		|------>NULL
		

 */

bool_t xdr_build_lookup(void)
{
	bool_t bRetVal = TRUE;
	if(tableBase == NULL)
	{
		tableBase = (struct xdr_discrim***)capi2_malloc(256*sizeof(struct xdr_discrim**));
		if(tableBase)
		{
			int i;
			for(i=0; i < 256; i++)
				tableBase[i] = NULL;
			for(i=0; i < (int)(sizeof(CAPI2_ReqRep_dscrm)/sizeof(struct  xdr_discrim)); i++)
			{
				unsigned short id = (unsigned short)CAPI2_ReqRep_dscrm[i].value;
				unsigned short id1 = ( (unsigned short)(( id & (unsigned short)0xff00 ) >> 8));
				unsigned short id2 = id & ((unsigned short)0x00ff);
				struct xdr_discrim** tableBase2 = NULL;
				if(tableBase[id1] == NULL)
				{
					int j;
					tableBase[id1] = (struct xdr_discrim**)capi2_malloc(256*sizeof(struct xdr_discrim*));
					tableBase2 = tableBase[id1];
					for(j=0; j < 256; j++)
						tableBase2[j] = NULL;
				}
				if(tableBase[id1] == NULL)
				{
					bRetVal = FALSE;
				}
				else
				{
					tableBase2 = tableBase[id1];
					if(tableBase2[id2] != NULL &&
						(id != MSG_SIM_EFILE_DATA_RSP && id != MSG_SIM_EFILE_UPDATE_RSP && id != MSG_PLMNLIST_IND) )
						_DBG_(CAPI2_TRACE("xdr_build_lookup duplicate entry = 0x%x, 0x%x, 0x%x\r\n", id, id1, id2));
					tableBase2[id2] = (struct xdr_discrim*)&CAPI2_ReqRep_dscrm[i];
					//_DBG_(CAPI2_TRACE("xdr_build_lookup = 0x%x, 0x%x, 0x%x\r\n", id, id1, id2));
				}
			}
		}
		else
		{
			bRetVal = FALSE;
		}
	}
	return bRetVal;
}

struct xdr_discrim* xdr_fast_lookup(enum_t dscm)
{
	struct xdr_discrim* nodePtr = NULL;
	unsigned short id = (unsigned short)dscm;
	unsigned short id1 = ((unsigned short)( (id & (unsigned short)(((unsigned short)0xff00))) >> 8));
	unsigned short id2 = id & ((unsigned short)0x00ff);
	struct xdr_discrim** tableBase2 = NULL;
	
	/* Make sure the table is built up */
	if(xdr_build_lookup() == FALSE)
		return NULL;

	tableBase2 = tableBase[id1];
	/* maximum message type is 0xffff */
	if(dscm > ((enum_t)0xffff))
	{
		/* should assert here */
		_DBG_(CAPI2_TRACE("xdr_fast_lookup no CAP2 entry = 0x%x, 0x%x\r\n", id, dscm));
	}
	else if(tableBase2 == NULL)
	{
		/* message not handled in CAPI2 */
		_DBG_(CAPI2_TRACE("xdr_fast_lookup no CAP2 entry = 0x%x, 0x%x, 0x%x\r\n", id, id1, id2));
	}
	else if(tableBase2[id2] != NULL)
		nodePtr = tableBase2[id2];
	else
	{
		/* message not handled in CAPI2 */
		_DBG_(CAPI2_TRACE("xdr_fast_lookup no CAP2 entry = 0x%x, 0x%x, 0x%x\r\n", id, id1, id2));
		return NULL;
	}
	/* message type sanity check invariant */
	if(nodePtr && nodePtr->value != dscm)
	{
		/* should assert here */
		_DBG_(CAPI2_TRACE("xdr_fast_lookup ERROR!!! = 0x%x, 0x%x\r\n", id, dscm));
		return NULL;
	}
	return nodePtr;
}

